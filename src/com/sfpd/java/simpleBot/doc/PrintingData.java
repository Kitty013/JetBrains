package com.sfpd.java.simpleBot.doc;

public class PrintingData {
	public static void main(String[] args) {
		/** The println method displays the passed string followed by a new line on the screen.
		 */
		System.out.println("I");
		System.out.println("know");
		System.out.println("Java");
		System.out.println("well");
		System.out.println(); // prints empty line

		/** This method allows you to print an empty line when no string is given.
		 */
		System.out.println("Java is a popular programming language");
		System.out.println(); // prints empty line
		System.out.println("It is used all over the world!");
		System.out.println();

		/** The print method displays the value that was passed in and places the cursor after it.
		 */
		System.out.print("I ");
		System.out.print("know ");
		System.out.print("Java ");
		System.out.print("well.");
		System.out.println();

		/** Printing numbers and characters. Let's print two secret codes.
		 */
		System.out.println();
		System.out.print(108);   // printing a number
		System.out.print('c');   // printing a character that represents a letter
		System.out.print("Q");   // printing a string
		System.out.println('3'); // printing a character that represents a digit

		System.out.print(22);
		System.out.print('E');
		System.out.print(8);
		System.out.println('1');
		System.out.println();

		/** Exercise : write a program that prints following text:
		 * 		WE NEED TO
		 * 		
		 * 		LEARN JAVA
		 * 	
		 * 		AS QUICKLY AS POSSIBLE
		 */
			System.out.println("WE NEED TO");
		System.out.println();
		System.out.println("LEARN JAVA");
		System.out.println();
		System.out.println("AS QUICKLY AS POSSIBLE");

		System.out.println();
	}
	
}
