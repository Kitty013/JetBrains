package com.sfpd.java.introductionToJavaProgrammingAndDataStructures.introductionToComputersProgramsAndJava.src;

public class PrintATable {
    /** Write a program that displays the following table:
      *     a   a^2     a^3     a^4
      *     1    1       1       1
      *     2    4       8       16
      *     3    9       27      81
      *     4    16      64      256
     */
    public static void main(String[] args) {
        System.out.println(" a " + " a^2" + " a^3 " + " a^4 " );
        System.out.println(" 1 " + "  1 " + "  1  " + "  1  ");
        System.out.println(" 2 " + "  4 " + "  8  " + "  16 ");
        System.out.println(" 3 " + "  9 " + "  27 " + "  81 ");
        System.out.println(" 4 " + " 16 " + "  64 " + " 256 ");

        // Solution from the book
        System.out.println("a      a^2    a^3    a^4");
        System.out.println("1      1      1      1");
        System.out.println("2      4      8      16");
        System.out.println("3      9      27     81");
        System.out.println("4      16     64     256");

    }
}
